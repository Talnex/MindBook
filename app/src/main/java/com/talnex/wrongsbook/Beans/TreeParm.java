package com.talnex.wrongsbook.Beans;

public class TreeParm {
    private int offset;

    private int leftpoint_x;
    private int leftpoint_y;

    private int rightpoint_x;
    private int rightpoint_y;

    private int center_x;
    private int center_y;

    private int hight;
    private int width;

    public int getWidth() {
        return width;
    }

    public void setWidth(int width) {
        this.width = width;
    }

    public TreeParm() {
    }

    public int getHight() {
        return hight;
    }

    public void setHight(int hight) {
        this.hight = hight;
    }

    public int getOffset() {
        return offset;
    }

    public void setOffset(int offset) {
        this.offset = offset;
    }

    public int getLeftpoint_x() {
        return leftpoint_x;
    }

    public void setLeftpoint_x(int leftpoint_x) {
        this.leftpoint_x = leftpoint_x;
    }

    public int getLeftpoint_y() {
        return leftpoint_y;
    }

    public void setLeftpoint_y(int leftpoint_y) {
        this.leftpoint_y = leftpoint_y;
    }

    public int getRightpoint_x() {
        return rightpoint_x;
    }

    public void setRightpoint_x(int rightpoint_x) {
        this.rightpoint_x = rightpoint_x;
    }

    public int getRightpoint_y() {
        return rightpoint_y;
    }

    public void setRightpoint_y(int rightpoint_y) {
        this.rightpoint_y = rightpoint_y;
    }

    public int getCenter_x() {
        return center_x;
    }

    public void setCenter_x(int center_x) {
        this.center_x = center_x;
    }

    public int getCenter_y() {
        return center_y;
    }

    public void setCenter_y(int center_y) {
        this.center_y = center_y;
    }
}
